package bankdroid.smskey.github;

public class CreateIssueResponse {
	private int id;
	private String url;
	private String html_url;
	private int number;

	public int getId() {
		return id;
	}

	public String getUrl() {
		return url;
	}

	public String getHtmlUrl() {
		return html_url;
	}

	public int getNumber() {
		return number;
	}
}
